    private void tblComprasUnitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblComprasUnitMouseClicked

        if (evt.getButton() == MouseEvent.BUTTON1 && evt.getClickCount() == 2) {
            txtCantidadVentaPro.setText("");
            int fila = tblComprasUnit.rowAtPoint(evt.getPoint());
            int columna = tblComprasUnit.columnAtPoint(evt.getPoint());
            if ((fila > -1) && (columna > -1)) {
                pr.setId(Integer.parseInt(tblComprasUnit.getValueAt(fila, 0).toString()));
                pr.setNombre(tblComprasUnit.getValueAt(fila, 1).toString());
                pr.setPrecio(Double.parseDouble(tblComprasUnit.getValueAt(fila, 4).toString()));
                int stock = Integer.parseInt(tblComprasUnit.getValueAt(fila, 5).toString());

                if (stock > 0) {

                    txtCodVentaPro.setText(String.valueOf(pr.getId()));
                    txtNombreVentaPro.setText(pr.getNombre());
                    txtPrecioProVen.setText(String.valueOf(pr.getPrecio()));

                    txtCantidadVentaPro.addKeyListener(new KeyAdapter() {
                        @Override
                        public void keyReleased(KeyEvent e) {
                            // Obtén la cantidad ingresada por el usuario
                            try {
                                int cantidad = Integer.parseInt(txtCantidadVentaPro.getText());
                                double precio = Double.parseDouble(txtPrecioProVen.getText());

                                if (cantidad > stock) {
                                    // Si la cantidad es mayor que el stock, muestra un mensaje de alerta
                                    JOptionPane.showMessageDialog(null, "La cantidad Excede al stock del producto", "VENTA", JOptionPane.WARNING_MESSAGE);
                                    txtCantidadVentaPro.setText("");
                                } else {
                                    // Realiza el cálculo basado en la cantidad ingresada y el precio
                                    double total = cantidad * precio;
                                    txtTotalVenta.setText(String.valueOf(total));
                                }
                            } catch (NumberFormatException ex) {
                                // Manejar la excepción si la entrada no es un número válido
                                txtTotalVenta.setText(""); // Puedes establecer un valor predeterminado o mostrar un mensaje de error
                            }
                        }
                    });

                } else {
                    JOptionPane.showMessageDialog(null, "No hay stock", "VENTA", JOptionPane.WARNING_MESSAGE);
                }

            }

        }

    }//GEN-LAST:event_tblComprasUnitMouseClicked

