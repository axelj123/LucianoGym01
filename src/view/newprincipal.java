    private void btnRegistrarDNIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarDNIActionPerformed
        if (txtDni.isEnabled()) {
            // Si está habilitado, deshabilítalo

            btnRegistrarDNI.setText("HABILITAR");
            btnRegistrarDNI.setEnabled(false);
            txtDni.setEnabled(false);
        } else {
            // Si está deshabilitado, habilita el campo DNI y coloca el foco en él
            btnRegistrarDNI.setEnabled(true);
            txtDni.setEnabled(true);

            btnRegistrarDNI.setText("DESHABILITAR");
            txtDni.requestFocus();
            txtDni.selectAll(); // Selecciona todo el texto existente, si lo hay
        }
        // Elimina cualquier mensaje de advertencia que pueda estar mostrándose
        clearDniWarning();
        txtDni.setText("");
        btnRegistrarDNI.setEnabled(true);
    }

// Método para eliminar el mensaje de advertencia del campo DNI
    private void clearDniWarning() {
        for (Window window : Window.getWindows()) {
            if (window instanceof JDialog) {
                JDialog dialog = (JDialog) window;
                if (dialog.getContentPane().getComponentCount() == 1
                        && dialog.getContentPane().getComponent(0) instanceof JOptionPane) {
                    dialog.dispose();
                }
            }
        }
    }//GEN-LAST:event_btnRegistrarDNIActionPerformed
