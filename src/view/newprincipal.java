    public void buscarClientes(String valor, JTable tblBuscar) {
        String sql = "SELECT u.Codigo, u.Nombres, u.Apellidos, e.PrecioMembresia, e.MontoPagado, e.MontoRestante "
                + "FROM PagoMembresias e "
                + "JOIN Usuarios u ON e.ClienteID = u.Codigo "
                + "WHERE u.Apellidos LIKE '" + valor + "%' AND e.MontoRestante > 0";

        conexion cn = new conexion();
        Connection con;
        PreparedStatement ps;
        ResultSet rs;
        DefaultTableModel model = (DefaultTableModel) tblBuscarPagos2.getModel();
        model.setRowCount(0);
        try {
            con = cn.getConexion();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            // Eliminar todas las filas existentes en la tabla

            while (rs.next()) {
                cl.setCodigo(rs.getInt("Codigo"));
                cl.setNombres(rs.getString("Nombres"));
                cl.setApellidos(rs.getString("Apellidos"));
                cl.setPrecioMembresia(rs.getDouble("PrecioMembresia"));
                cl.setMontoPagado(rs.getDouble("MontoPagado"));
                cl.setMontoRestante(rs.getDouble("MontoRestante"));

                String[] registros = {
                    String.valueOf(cl.getCodigo()),
                    cl.getNombres(),
                    cl.getApellidos(),
                    String.valueOf(cl.getPrecioMembresia()),
                    String.valueOf(cl.getMontoPagado()),
                    String.valueOf(cl.getMontoRestante()),};

                model.addRow(registros);
            }
            int ca = tblBuscar.getRowCount();
            lblPagosPendientes.setText(String.valueOf(ca));

        } catch (SQLException ex) {
            System.out.print(ex);
        }
    }
